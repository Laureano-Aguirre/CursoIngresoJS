function mostrar()
{
	//declaramos las variables
	let mes;


	//definimos las variables
	mes=document.getElementById('txtIdMes').value;

	//declaramos el switch
	switch (mes) {
		case "Enero":
			alert("Que comiences bien el año!!!");
			break;
		case "Marzo":
			alert("A clases!!!");
			break;
		case "Julio":
			alert("Se vienen las vacaciones!!!");
			break;
		case "Diciembre":
			alert("Felices fiestas!!!");
			break;
	
		
	}


}//FIN DE LA FUNCIÓN